import Foundation

extension Int32 {
	
	/** Get a string from the integer. */
	func toString() -> String {
		return "\(self)"
	}
	
}

extension Int {
	
	/** Get a string from the integer. */
	func toString() -> String {
		return "\(self)"
	}
	
}
