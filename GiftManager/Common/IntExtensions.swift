import Foundation

extension Int32 {
	
	/// Get a string from the integer.
	///
	/// - Returns: the integer as a string
	func toString() -> String {
		return "\(self)"
	}
	
}

extension Int {
	
	/// Get a string from the integer.
	///
	/// - Returns: the integer as a string
	func toString() -> String {
		return "\(self)"
	}
	
}
